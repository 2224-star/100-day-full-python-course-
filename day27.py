### ... A tuple in python in and ordered , immutable collection of items. Once create, you 
# cannot change , add, or remove items from a tuple.

# create a tuple
colors = ("white","black","red","blue")
print(colors) # Output: ('white', 'black', 'red','blue')

##.. Accessing Elements

print(colors[0]) # output the first item: 'white'
print(colors[-1]) # output the last item: 'blue'

print(colors.count())
print(colors.index("red"))


