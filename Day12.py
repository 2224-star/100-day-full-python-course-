# # this is the  String Slicing In Python programming 
### String Slicint is a way to get a part (substring) of a string using the syntax:
# # string[start:end:step]
## start index to strt form (inclusive)
## stop:index to stop at exclusive
## step: how many steps to jump (default is 1)
## Exmples
text = "Hello, Python!"
print(text) # Output: Hello,pyton!
## and second example is 
print(text[0:5]) # Output: Hello
print(text[0:4])
print(text[-5:13]) # Output: thon 
